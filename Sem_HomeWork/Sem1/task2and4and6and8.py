# Задача 6: Вы пользуетесь общественным транспортом? Вероятно, вы
# расплачивались за проезд и получали билет с номером. Счастливым
# билетом называют такой билет с шестизначным номером, где сумма
# первых трех цифр равна сумме последних трех. Т.е. билет с номером
# 385916 – счастливый, т.к. 3+8+5=9+1+6. Вам требуется написать
# программу, которая проверяет счастливость билета.

number = int(input('Введите номер билета: '))
if number >= 100000 and number <= 999999:
    sum1 = 0
    sum2 = 0
    left = number // 1000
    right = number % 1000

    for i in range(3):
        sum1 += left % 10
        left //= 10
        sum2 += right % 10
        right //= 10
    if sum1 == sum2:
        print(f'Номер билета {number} – счастливый')
    else:
        print(f'Номер билета {number} – не счастливый')
else:
    print('такого билета нет')
print()

# Задача 8: Требуется определить, можно ли от шоколадки размером n × m долек
# отломить k долек, если разрешается сделать один разлом по
# прямой между дольками (то есть разломить шоколадку на два
# прямоугольника).
# 3 2 4 -> yes
# 3 2 1 -> no
n = int(input('Введите длину шоколадки: '))
m = int(input('Введите ширину шоколадки: '))
k = int(input('Введите количество долек: '))

if n * m >= k:
    if k % m == 0 or k % n == 0:
        print(f'Можно разломать шоколадку на {k} долек')
    else:
        print(f'Нельзя разломать шоколадку на {k} долек')
else:
    print(f'Нельзя разломать шоколадку на {k} долек')
