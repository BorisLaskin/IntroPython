# Задача 2: Найдите сумму цифр трехзначного числа.

number = abs(int(input('Введите число: ')))
sum = 0
if number > 99 and number < 1000:
    while number > 0:
        sum += number % 10
        number //= 10
    print(f'Сумма цифр трехзначного числа:{sum}')
else:
    print('Число должно быть больше 99 и меньше 1000')
print()

